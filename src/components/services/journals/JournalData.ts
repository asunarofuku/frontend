export function groupJournalFilesByYear() {

  const data = journalFiles.reduce<Record<number, PdfFile[]>>(
    (acc, post: PdfFile) => {
      if (!acc[post.year]) {
        acc[post.year] = [];
      }
      acc[post.year].push(post);
      return acc;
    },
    {},
  );
  return Object.entries(data).map(([year, files]) => {
    return [parseInt(year), files.sort((a, b) => a.month - b.month)];
  }).sort(([a], [b]) => b - a);
}
